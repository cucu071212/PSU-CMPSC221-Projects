    private void addScheduleClassesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addScheduleClassesSubmitButtonActionPerformed
        String scheduleClass = ScheduleClassComboBox.getSelectedItem().toString();
        StudentEntry scheduleStudent = (StudentEntry) ScheduleStudentComboBox.getSelectedItem();
        String studentID = scheduleStudent.getStudentID();
        String scheduleStatus = "scheduled";
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        if (ClassQueries.getClassSeats(currentSemester, scheduleClass) <= ScheduleQueries.getScheduledStudentCount(currentSemester, scheduleClass)){
            scheduleStatus = "waitlisted";}

        ScheduleEntry schedule = new ScheduleEntry(currentSemester, scheduleClass, studentID, scheduleStatus, currentTimestamp);
        ScheduleQueries.addScheduleEntry(schedule);
        addScheduleClassesStatusLabel.setText(scheduleStudent + " has been " + scheduleStatus + " for the class");
        
            
    }//GEN-LAST:event_addScheduleClassesSubmitButtonActionPerformed

