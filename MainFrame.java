    private void StudentDropClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StudentDropClassSubmitButtonActionPerformed
        StudentEntry student = (StudentEntry) DropClassStudentComboBox.getSelectedItem();
        String studentID = student.getStudentID();
        String courseCode = StudentDropClassClassComboBox.getSelectedItem().toString();
        
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester, studentID);
        String status = "";
        for (ScheduleEntry entry : schedule) {
            if (entry.getCourseCode().equals(courseCode)) {
                status = entry.getStatus();
                break;
            }   
        }
        
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, studentID, courseCode);
        if (status.equals("scheduled")) {
            StudentDisplayDropClassStatusTextArea.setText(student + " has been dropped from " + courseCode + "\n");
            
            ArrayList<ScheduleEntry> studentschedule = ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, courseCode);
            
            if(!studentschedule.isEmpty()){
                ScheduleQueries.updateScheduleEntry(studentschedule.getFirst());
                StudentDisplayDropClassStatusTextArea.append(StudentQueries.getStudent(studentschedule.getFirst().getStudentID()) + " has been scheduled into " + courseCode);
                
                   
            }

        }
        
        else {
            StudentDisplayDropClassStatusTextArea.setText(student + " has been dropped from " + courseCode);
        }



    }//GEN-LAST:event_StudentDropClassSubmitButtonActionPerformed
                              
    private void checkData() {
        try {
            FileReader reader = new FileReader("xzq789yy.txt");
            BufferedReader breader = new BufferedReader(reader);

            String encodedAuthor = breader.readLine();
            String encodedProject = breader.readLine();
            byte[] decodedAuthor = Base64.getDecoder().decode(encodedAuthor);
            author = new String(decodedAuthor);
            byte[] decodedProject = Base64.getDecoder().decode(encodedProject);
            projectDate = new String(decodedProject);
            reader.close();

        } catch (FileNotFoundException e) {
            //get user info and create file
            author = JOptionPane.showInputDialog("Enter your first and last name.");
            projectDate = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime()).toString();

            //write data to the data file.
            try {
                FileWriter writer = new FileWriter("xzq789yy.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(writer);

                // encode the output data.
                String encodedAuthor = Base64.getEncoder().encodeToString(author.getBytes());

                bufferedWriter.write(encodedAuthor);
                bufferedWriter.newLine();

                String encodedProject = Base64.getEncoder().encodeToString(projectDate.getBytes());
                bufferedWriter.write(encodedProject);

                bufferedWriter.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                System.exit(1);
            }

        } catch (IOException e) {
            e.printStackTrace();
            System.exit(1);
        }

    }

