    private void DropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentSubmitButtonActionPerformed
        StudentEntry student = (StudentEntry) DropStudentComboBox.getSelectedItem();
        DropStudentStatusTextArea.append(student.toString() + " has been dropped from the list of students.\n\n");
        StudentQueries.dropStudent(student.getStudentID());
        ArrayList<SemesterEntry> semesters = SemesterQueries.getSemesterList();
        for (SemesterEntry semesterEntry : semesters) {
            DropStudentStatusTextArea.append("For Semester: " + semesterEntry.toString() + "\n");
            
            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semesterEntry.toString(), student.getStudentID());
            String status = "scheduled";
            for (ScheduleEntry scheduleEntry : schedules) {
                if (scheduleEntry.getStatus().equals(status)) {
                    DropStudentStatusTextArea.append(student.toString() + " has been dropped from " + scheduleEntry.getCourseCode() + "\n");
                    ScheduleQueries.dropStudentScheduleByCourse(semesterEntry.toString(), student.getStudentID(), scheduleEntry.getCourseCode());
                    if (!MultiTableQueries.getWaitlistedStudentsByClass(semesterEntry.toString(), scheduleEntry.getCourseCode()).isEmpty()) {
                        DropStudentStatusTextArea.append(MultiTableQueries.getWaitlistedStudentsByClass(semesterEntry.toString(), scheduleEntry.getCourseCode()).get(0).toString() + " has been scheduled into " + scheduleEntry.getCourseCode() + "\n");
                        ScheduleQueries.updateScheduleEntry(ScheduleQueries.getWaitlistedStudentsByClass(semesterEntry.toString(), scheduleEntry.getCourseCode()).get(0));

                    }
                }        
                else {
                    DropStudentStatusTextArea.append(student.toString() + " has been dropped from the waitlist for " + scheduleEntry.getCourseCode() + "\n");
                    ScheduleQueries.dropStudentScheduleByCourse(semesterEntry.toString(), student.getStudentID(), scheduleEntry.getCourseCode());
                }
                
            }
            DropStudentStatusTextArea.append("\n\n");
        }
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_DropStudentSubmitButtonActionPerformed

